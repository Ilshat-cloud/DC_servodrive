int main(void)
{

  /* USER CODE BEGIN 1 */

  /* USER CODE END 1 */

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_DMA_Init();
  MX_ADC1_Init();
  MX_IWDG_Init();
  MX_SPI1_Init();
  MX_TIM1_Init();
  MX_TIM2_Init();
  MX_TIM3_Init();
  MX_TIM4_Init();
  /* USER CODE BEGIN 2 */
  init_state=(HAL_GPIO_ReadPin(DIP01_GPIO_Port,DIP01_Pin)&0x01)|((HAL_GPIO_ReadPin(DIP10_GPIO_Port,DIP10_Pin)&0x01)<<01);
  //check this one if you will generate code again
  switch (init_state){
    case 0:
      /* creation of Debug_mode */
        M1.D_position=0;
        M1.I_position=0;
        M1.P_position=0;
        M1.velocity_sp=100;

        M1.D_velocity=1;
        M1.I_velocity=10;
        M1.P_velocity=5;
        M1.D_current=1;
        M1.I_current=10;
        M1.P_current=5;
      break;
    case 1:
      /* creation of Step_DIR */
      MX_TIM3_Init();
      break;
    case 2:
      /* creation of ManualTask */

      break;
    case 3:
      /* creation of SPI */
      MX_SPI1_Init();
      break;
      
    }  
  HAL_ADC_Stop(&hadc1);
  HAL_ADC_Stop_DMA(&hadc1);
  HAL_ADC_Start_DMA(&hadc1,(uint32_t*)&dma,2);
  HAL_TIM_Base_Start_IT(&htim4);
  HAL_TIM_Encoder_Start(&htim1,TIM_CHANNEL_ALL);
  HAL_Delay(1);
  uint16_t dma_I_prev=dma[0],dma_V_prev=dma[1];
  uint8_t tic_prev=tic_count;
  TIM2->CCR1=0;
  TIM2->CCR2=0;
  M1.curr_direction=1;
  HAL_TIM_PWM_Start(&htim2,TIM_CHANNEL_ALL);
  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {
    if (tic_prev!=tic_count){ //100hz
      HAL_IWDG_Refresh(&hiwdg);
      tic_prev=tic_count;
      M1.I_M=(dma_I_prev+dma[0])>>1;
      dma_I_prev=dma[0];
      
      switch (init_state){
      case 3:
        /* creation of Debug_mode */
        PID_REG(&M1); // 0.01 sec
        PWM_out_H_brige(-500);
        break;
      case 1:
        /* creation of Step_DIR */
        if (HAL_GPIO_ReadPin(EN_GPIO_Port,EN_Pin)==GPIO_PIN_SET)
        {
          if (HAL_GPIO_ReadPin(DIR_GPIO_Port,DIR_Pin)==GPIO_PIN_SET)
          {
            if (Step_cnt_from_EXTI>0&&Step_cnt_from_EXTI<5000){
              M1.position_sp+=Step_cnt_from_EXTI;
              Step_cnt_from_EXTI=0;
            }
          }else{
            if (Step_cnt_from_EXTI>0&&Step_cnt_from_EXTI<5000){
              M1.position_sp-=Step_cnt_from_EXTI;
              Step_cnt_from_EXTI=0;
            }
          }
          PID_REG(&M1); // 0.01 sec
          PWM_out_H_brige(M1.PWM_out);
        } else {
          PWM_out_H_brige(0);
          Step_cnt_from_EXTI=0;
        }
        break;
      case 2:
        /* creation of ManualTask */
        if (HAL_GPIO_ReadPin(EN_GPIO_Port,EN_Pin)==GPIO_PIN_SET)
        {
          if (HAL_GPIO_ReadPin(DIR_GPIO_Port,DIR_Pin)==GPIO_PIN_SET)
          {
            M1.velocity_sp=(M1.velocity_max*(dma_V_prev+dma[1]))>>13;  //div 8096
            dma_V_prev=dma[1];
            M1.velocity_sp=(M1.velocity_sp>M1.velocity_max)?M1.velocity_max:M1.velocity_sp;
          }else{
            M1.velocity_sp=-((M1.velocity_max*(dma_V_prev+dma[1]))>>13);
            dma_V_prev=dma[1];
            M1.velocity_sp=(M1.velocity_sp*(-1)>(M1.velocity_max))?(M1.velocity_max*(-1)):M1.velocity_sp;
          }
          PID_REG(&M1); // 0.01 sec
          
          PWM_out_H_brige(M1.PWM_out);
        }else{
          PWM_out_H_brige(0);
        }
        break;
      case 0:
        /* creation of SPI */
        
        break;
        
      }
      HAL_ADC_Stop_DMA(&hadc1);
      HAL_ADC_Start_DMA(&hadc1,(uint32_t*)&dma,2);
    }
    /* USER CODE END WHILE */

    /* USER CODE BEGIN 3 */
  }
  /* USER CODE END 3 */
}

